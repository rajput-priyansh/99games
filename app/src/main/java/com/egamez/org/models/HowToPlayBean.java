package com.egamez.org.models;

public class HowToPlayBean {
    private String youtube_link_id;
    private String youtube_link;
    private String youtube_link_title;
    private String date_created;

    public String getYoutube_link_id() {
        return youtube_link_id;
    }

    public void setYoutube_link_id(String youtube_link_id) {
        this.youtube_link_id = youtube_link_id;
    }

    public String getYoutube_link() {
        return youtube_link;
    }

    public void setYoutube_link(String youtube_link) {
        this.youtube_link = youtube_link;
    }

    public String getYoutube_link_title() {
        return youtube_link_title;
    }

    public void setYoutube_link_title(String youtube_link_title) {
        this.youtube_link_title = youtube_link_title;
    }

    public String getDate_created() {
        return date_created;
    }

    public void setDate_created(String date_created) {
        this.date_created = date_created;
    }
}
